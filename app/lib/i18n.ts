export function getTranslation(locale: Locale, key: string): string {
  const keys = key.split('.');
  let result: any = translations[locale];
  for (const k of keys) {
    result = result?.[k];
    if (!result) break;
  }
  if (!result && locale !== 'en') {
    result = translations.en[key as keyof typeof translations.en];
  }
  return result || key;
}